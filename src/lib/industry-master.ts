/** Geminiプロンプト用: 大分類・小分類の一覧テキスト */
export function getIndustryMasterPromptText(): string {
  const lines: string[] = [];
  for (const [major, minors] of Object.entries(INDUSTRY_MASTER)) {
    lines.push(`${major}: ${minors.join(' / ')}`);
  }
  return lines.join('\n');
}
